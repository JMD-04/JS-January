const city = "Kanpur"
switch (city) {
    case "noida":
        console.log("1");
        break;
    case "gurgoan":
        console.log("2");
        break;
    case "Kanpur":
        console.log("3");
        break;
    case "mumbai":
        console.log("4");
        break;
    case "delhi":
        console.log("5");
        break;       
    default:
        console.log("6");
        break;
}




